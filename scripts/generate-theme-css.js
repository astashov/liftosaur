const fs = require("fs");
const path = require("path");

const baseColors = require("../tailwind.colors.json");
const semantic = require("../tailwind.semantic.generated.json");

function flatten(obj, prefix) {
  const out = {};
  for (const [k, v] of Object.entries(obj)) {
    const name = prefix ? `${prefix}-${k}` : k;
    if (v && typeof v === "object") {
      Object.assign(out, flatten(v, name));
    } else {
      out[name] = v;
    }
  }
  return out;
}

function emitVars(vars, indent) {
  return Object.entries(vars)
    .map(([k, v]) => `${indent}--color-${k}: ${v};`)
    .join("\n");
}

const baseFlat = flatten(baseColors, "");
const lightFlat = flatten(semantic.light, "");
const darkFlat = flatten(semantic.dark, "");

const css = `/* Generated by scripts/generate-theme-css.js — do not edit by hand. */
@source inline("line-clamp-{1,2,3,4,5,6,none}");

@theme {
  --spacing: 4px;
  --text-xs: 12px;
  --text-sm: 14px;
  --text-base: 16px;
  --text-lg: 18px;
  --text-xl: 20px;
  --text-2xl: 24px;
  --text-3xl: 30px;
  --text-4xl: 36px;
  --text-5xl: 48px;
  --font-sans: Poppins, sans-serif;
  --font-serif: Poppins, serif;
  --radius-xl: 0.75rem;
  --radius-2xl: 1rem;
  --radius-3xl: 1.5rem;

${emitVars(baseFlat, "  ")}

${emitVars(lightFlat, "  ")}
}

@layer theme {
  :root {
    @variant light {
${emitVars(lightFlat, "      ")}
    }

    @variant dark {
${emitVars(darkFlat, "      ")}
    }
  }
}
`;

const outPath = path.resolve(__dirname, "..", "theme.generated.css");
fs.writeFileSync(outPath, css);
console.log(`Wrote ${outPath} (${css.length} bytes)`);

const typesContent = `// NOTE: This file is generated by scripts/generate-theme-css.js — do not edit by hand.
/// <reference types="uniwind/types" />

declare module 'uniwind' {
    export interface UniwindConfig {
        themes: readonly ['light', 'dark']
    }
}

export {}
`;
const typesPath = path.resolve(__dirname, "..", "uniwind-types.d.ts");
fs.writeFileSync(typesPath, typesContent);
console.log(`Wrote ${typesPath} (${typesContent.length} bytes)`);
